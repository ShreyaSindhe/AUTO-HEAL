# HELPER: simulate one full self-healing cycle for a dataset
# ==================================================================
def run_autoheal_cycle(X, y, run_seed, window=50, threshold=0.75,
                        consec_k=3, noise_mean=5.0, noise_std=3.0,
                        n_clean=80, n_drift=80, n_recovery=80):
    """
    Runs the full AutoHeal cycle:
      Phase 1 - clean data  (model predicts well)
      Phase 2 - drifted data (accuracy drops)
      Phase 3 - recovery data (after auto-retrain)

    Returns a dict with all metrics and the full accuracy log.
    """

    # -- Train initial model ---------------------------------------
    X_train, X_test, y_train, y_test = train_test_split(
        X, y, test_size=0.2, random_state=run_seed, stratify=y
    )
    model = RandomForestClassifier(n_estimators=100, random_state=42)
    model.fit(X_train, y_train)
    baseline_acc = accuracy_score(y_test, model.predict(X_test))

    # -- Prediction buffer -----------------------------------------
    pred_buffer = []   # stores (true, predicted) tuples
    accuracy_log = []  # stores (phase, rolling_acc) tuples

    def rolling_accuracy():
        if len(pred_buffer) < window:
            return 1.0
        recent = pred_buffer[-window:]
        return np.mean([int(t == p) for t, p in recent])

    # -- Phase 1: Clean predictions --------------------------------
    rng = np.random.RandomState(run_seed * 100)
    for _ in range(n_clean):
        idx = rng.randint(len(X_test))
        pred = model.predict(X_test[idx].reshape(1, -1))[0]
        pred_buffer.append((y_test[idx], pred))
        if len(pred_buffer) >= window:
            accuracy_log.append(("clean", rolling_accuracy()))

    # -- Phase 2: Drifted predictions -----------------------------
    drift_trigger_idx = None
    fail_count = 0
    for i in range(n_drift):
        idx = rng.randint(len(X_test))
        noisy_x = X_test[idx] + rng.normal(noise_mean, noise_std, X_test[idx].shape)
        pred = model.predict(noisy_x.reshape(1, -1))[0]
        pred_buffer.append((y_test[idx], pred))
        if len(pred_buffer) >= window:
            acc = rolling_accuracy()
            accuracy_log.append(("drift", acc))
            # Drift detection logic
            if acc < threshold:
                fail_count += 1
                if fail_count >= consec_k and drift_trigger_idx is None:
                    drift_trigger_idx = len(accuracy_log) - 1
            else:
                fail_count = 0

    min_drift_acc = min(
        [a for ph, a in accuracy_log if ph == "drift"], default=baseline_acc
    )

    # -- Retrain on fresh clean data -------------------------------
    X_tr2, X_te2, y_tr2, y_te2 = train_test_split(
        X, y, test_size=0.2, random_state=run_seed + 50, stratify=y
    )
    new_model = RandomForestClassifier(n_estimators=100, random_state=42)
    new_model.fit(X_tr2, y_tr2)
    retrain_acc = accuracy_score(y_te2, new_model.predict(X_te2))

    # Deploy if better (with tolerance 0.02)
    deployed_model = new_model if retrain_acc >= baseline_acc - 0.02 else model
    redeployed = retrain_acc >= baseline_acc - 0.02

    # -- Phase 3: Recovery predictions ----------------------------
    for _ in range(n_recovery):
        idx = rng.randint(len(X_test))
        pred = deployed_model.predict(X_test[idx].reshape(1, -1))[0]
        pred_buffer.append((y_test[idx], pred))
        if len(pred_buffer) >= window:
            accuracy_log.append(("recovery", rolling_accuracy()))

    recovery_acc = np.mean(
        [a for ph, a in accuracy_log if ph == "recovery"]
    ) if any(ph == "recovery" for ph, a in accuracy_log) else 0.0

    return {
        "baseline_acc":      round(baseline_acc, 4),
        "min_drift_acc":     round(min_drift_acc, 4),
        "retrain_acc":       round(retrain_acc, 4),
        "recovery_acc":      round(recovery_acc, 4),
        "drift_trigger_idx": drift_trigger_idx,
        "redeployed":        redeployed,
        "accuracy_log":      accuracy_log,
    }


