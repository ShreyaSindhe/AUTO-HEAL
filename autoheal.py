print("\n" + "─" * 65)
print("  EXPERIMENT 3: AutoHeal vs No-Monitor vs Scheduled Retrain")
print("─" * 65)

X_tr, X_te, y_tr, y_te = train_test_split(
    X_bc, y_bc, test_size=0.2, random_state=0, stratify=y_bc
)
rng = np.random.RandomState(42)

def make_noisy(x): return x + rng.normal(5.0, 3.0, x.shape)

# - Method 1: No monitoring ------------------------------------------
m_base = RandomForestClassifier(n_estimators=100, random_state=42).fit(X_tr, y_tr)
no_mon_correct = []
for _ in range(100):
    idx = rng.randint(len(X_te))
    pred = m_base.predict(make_noisy(X_te[idx]).reshape(1, -1))[0]
    no_mon_correct.append(int(pred == y_te[idx]))
no_mon_acc = round(np.mean(no_mon_correct), 4)

# - Method 2: Scheduled retraining (every 50 predictions) -----------
m_sched = RandomForestClassifier(n_estimators=100, random_state=42).fit(X_tr, y_tr)
sched_correct = []
for i in range(100):
    if i == 50:
        # Scheduled retrain at midpoint
        m_sched = RandomForestClassifier(n_estimators=100, random_state=42).fit(X_tr, y_tr)
    idx = rng.randint(len(X_te))
    pred = m_sched.predict(make_noisy(X_te[idx]).reshape(1, -1))[0]
    sched_correct.append(int(pred == y_te[idx]))
sched_acc = round(np.mean(sched_correct), 4)

# - Method 3: AutoHeal ----------------------------------------------
m_ah = RandomForestClassifier(n_estimators=100, random_state=42).fit(X_tr, y_tr)
ah_correct = []
ah_retrain_count = 0
ah_buffer = []
fail_count = 0
for i in range(100):
    idx = rng.randint(len(X_te))
    noisy_x = make_noisy(X_te[idx])
    pred = m_ah.predict(noisy_x.reshape(1, -1))[0]
    correct = int(pred == y_te[idx])
    ah_correct.append(correct)
    ah_buffer.append(correct)
    if len(ah_buffer) >= 20:
        if np.mean(ah_buffer[-20:]) < 0.75:
            fail_count += 1
            if fail_count >= 3:
                # Retrain on clean data
                m_ah = RandomForestClassifier(n_estimators=100, random_state=42).fit(X_tr, y_tr)
                ah_retrain_count += 1
                fail_count = 0
        else:
            fail_count = 0
ah_acc = round(np.mean(ah_correct), 4)

print(f"\n  No monitoring accuracy    : {no_mon_acc:.2%}  | Retrains: 0  | Human input: YES")
print(f"  Scheduled retrain accuracy: {sched_acc:.2%}  | Retrains: 1  | Human input: YES")
print(f"  AutoHeal accuracy         : {ah_acc:.2%}  | Retrains: {ah_retrain_count}  | Human input: NO")
print(f"\n  AutoHeal improvement over no-monitoring:    +{(ah_acc - no_mon_acc)*100:.1f} pp")
print(f"  AutoHeal improvement over scheduled retrain: +{(ah_acc - sched_acc)*100:.1f} pp")


